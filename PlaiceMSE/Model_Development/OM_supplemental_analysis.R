##### Exploratory work to ID appropriate high/low noise for BT anomaly projection

### Read in historic bottom temperature anomaly data
BT <- read.csv(paste(here::here(), "data", "MSE_data", "GLORYS_se.csv", sep="/")) # Same data explored in research track but naming standardized
BT <- BT %>% filter(Year>1979)

BT %>%
  ggplot() +
  geom_line(aes(x=Year, y=mean))

### Use 1984-1991 for low variability
## Std dev for noise around mean trend
BT %>%
  filter(Year>1983 & Year <1992) %>%
  summarize(meanSE = mean(se), meanSD = mean(sd)) # higher mean sd than high variability years

### Use 1992-2010 for high variability
## Std dev for for noise around mean trend
BT %>%
  filter(Year > 1991 & Year < 2011) %>%
  summarize(meanSE = mean(se), meanSD = mean(sd))

### Sort historic se from low to high
sortBT <- BT$se %>%
  c() %>%
  sort() 

# Average smallest 20 SEs
sortBT[1:20] %>%
  mean()
# Median of smallest 20 SEs #!!! USE MEDIAN to inform noise around env covariate mean trend
sortBT[1:20] %>%
  median()

# Average largest 20 SEs 
sortBT[21:40] %>%
  mean()
# Median of largest 20 SEs #!!! USE MEDIAN to inform noise around env covariate mean trend
sortBT[21:40] %>%
  median()

### Avg std error over whole time series (roughly splits difference between low and high variability periods above)
BT %>% 
  summarize(meanSE = mean(se)) 

## Look at last 10 years
BT %>%
  tail(n=10) %>%
  summarize(meanSE = mean(se)) # Same as rounded avg over whole time series, use SE = 0.03 in OM



##### Exploratory work to ID appropriate high/low noise for AMO projection
### Read in historic AMO data
# Load and filter environmental data to match year range of other model data
AMO <- read.csv(paste(here::here(), "data", "AMO_se.csv", sep="/"))
# Add placeholder for missing years of data
AMO <- rbind(c(1981, -999, -999, -999, -999), AMO) # 1981 years in the wrong order will cause R to bomb
AMO <- rbind(c(1980, -999, -999, -999, -999), AMO) # 1980
AMO <- rbind(c(1979, -999, -999, -999, -999), AMO) # 1979

AMO %>% filter(Year > 1981) %>%
  ggplot() +
  geom_line(aes(x=Year, y=mean))

#### Use same approach as BT to pick low & high SE for AMO
### Sort historic se from low to high
sortAMO <- AMO %>% filter(Year > 1981) %>% select(se) %>%
  unlist() %>%
  sort() 

# Average smallest 17 (first 3 years don't have data so fewer SEs in this calc) SEs
sortAMO[1:17] %>%
  mean()
# Median of smallest 20 SEs #!!! USE MEDIAN to inform noise around env covariate mean trend
sortAMO[1:17] %>%
  median()

# Average largest 20 SEs 
sortAMO[18:length(sortAMO)] %>%
  mean()
# Median of largest 20 SEs #!!! USE MEDIAN to inform noise around env covariate mean trend
sortAMO[18:length(sortAMO)] %>%
  median()

### Avg std error over whole time series (roughly splits difference between low and high variability periods above)
sortAMO %>% 
  summarize(meanSE = mean(se)) 

## Look at last 10 years
sortAMO %>%
  tail(n=10) %>%
  summarize(meanSE = mean(se)) # Same as rounded avg over whole time series, use SE = 0.03 in OM


